#!/usr/bin/env python3
"""
Script to generate random traces from C2PO files.

This script was generated by Cursor AI on 2026-01-01.
"""

import argparse
import csv
import random
import re
import sys
from pathlib import Path
from typing import List, Tuple


def parse_c2po_inputs(c2po_file: Path) -> List[Tuple[str, str]]:
    """
    Parse a C2PO file and extract input variable declarations.
    
    Returns a list of (variable_name, type) tuples.
    """
    variables = []
    
    with open(c2po_file, 'r') as f:
        content = f.read()
    
    # Find the INPUT section
    input_match = re.search(r'INPUT\s+(.*?)(?=\n\s*(?:FTSPEC|PTSPEC|DEFINE|STRUCT|$))', content, re.DOTALL)
    if not input_match:
        print(f"Error: No INPUT section found in {c2po_file}", file=sys.stderr)
        sys.exit(1)
    
    input_section = input_match.group(1)
    
    # Parse variable declarations
    # Pattern: variable_list: type;
    # Variable list can be: var1, var2, var3
    # Type can be: bool, int, float, or array types like int[10]
    decl_pattern = r'([a-zA-Z_][a-zA-Z0-9_]*(?:\s*,\s*[a-zA-Z_][a-zA-Z0-9_]*)*)\s*:\s*([a-zA-Z_][a-zA-Z0-9_\[\]]*)\s*;'
    
    for match in re.finditer(decl_pattern, input_section):
        var_list_str = match.group(1)
        type_str = match.group(2)
        
        # Split variable list by commas
        var_names = [v.strip() for v in var_list_str.split(',')]
        
        # Extract base type (handle arrays by taking the part before '[')
        base_type_match = re.match(r'([a-zA-Z_][a-zA-Z0-9_]*)', type_str)
        if base_type_match:
            base_type = base_type_match.group(1)
        else:
            base_type = type_str
        
        # Add each variable with its type
        for var_name in var_names:
            variables.append((var_name, base_type))
    
    return variables


def generate_trace(variables: List[Tuple[str, str]], length: int, min_val: float, max_val: float, output_file: Path):
    """
    Generate a random trace CSV file.
    
    Args:
        variables: List of (variable_name, type) tuples
        length: Number of time steps in the trace
        min_val: Minimum value for numeric types
        max_val: Maximum value for numeric types
        output_file: Path to output CSV file
    """
    with open(output_file, 'w', newline='') as f:
        writer = csv.writer(f)
        
        # Write header - format: # var1,var2,var3,...
        var_names = [var_name for var_name, _ in variables]
        f.write(f"#{','.join(var_names)}\n")
        
        # Generate random values for each time step
        for _ in range(length):
            row = []
            for var_name, var_type in variables:
                if var_type == 'bool':
                    # Random 0 or 1
                    row.append(random.randint(0, 1))
                elif var_type == 'int':
                    # Random integer between min_val and max_val (inclusive)
                    row.append(random.randint(int(min_val), int(max_val)))
                elif var_type == 'float':
                    # Random float between min_val and max_val
                    row.append(random.uniform(min_val, max_val))
                else:
                    print(f"Warning: Unknown type '{var_type}' for variable '{var_name}', treating as int", file=sys.stderr)
                    row.append(random.randint(int(min_val), int(max_val)))
            
            writer.writerow(row)


def main():
    parser = argparse.ArgumentParser(
        description='Generate a random trace CSV file from a C2PO specification file'
    )
    parser.add_argument(
        'c2po_file',
        type=Path,
        help='Path to the C2PO input file'
    )
    parser.add_argument(
        'length',
        type=int,
        help='Length of the trace (number of time steps)'
    )
    parser.add_argument(
        '--min',
        type=float,
        default=0.0,
        help='Minimum value for numeric types (default: 0.0)'
    )
    parser.add_argument(
        '--max',
        type=float,
        default=100.0,
        help='Maximum value for numeric types (default: 100.0)'
    )
    parser.add_argument(
        '-o', '--output',
        type=Path,
        help='Output CSV file path (default: <c2po_file_basename>_trace.csv)'
    )
    parser.add_argument(
        '--seed',
        type=int,
        help='Random seed for reproducibility'
    )
    
    args = parser.parse_args()
    
    # Set random seed if provided
    if args.seed is not None:
        random.seed(args.seed)
    
    # Validate inputs
    if not args.c2po_file.exists():
        print(f"Error: C2PO file not found: {args.c2po_file}", file=sys.stderr)
        sys.exit(1)
    
    if args.length <= 0:
        print(f"Error: Trace length must be positive, got {args.length}", file=sys.stderr)
        sys.exit(1)
    
    if args.min >= args.max:
        print(f"Error: min ({args.min}) must be less than max ({args.max})", file=sys.stderr)
        sys.exit(1)
    
    # Determine output file
    if args.output:
        output_file = args.output
    else:
        output_file = args.c2po_file.parent / f"{args.c2po_file.stem}_trace.csv"
    
    # Parse C2PO file
    try:
        variables = parse_c2po_inputs(args.c2po_file)
    except Exception as e:
        print(f"Error parsing C2PO file: {e}", file=sys.stderr)
        sys.exit(1)
    
    if not variables:
        print(f"Error: No input variables found in {args.c2po_file}", file=sys.stderr)
        sys.exit(1)
    
    # Generate trace
    try:
        generate_trace(variables, args.length, args.min, args.max, output_file)
        print(f"Generated trace with {len(variables)} variables and {args.length} time steps")
        print(f"Output written to: {output_file}")
    except Exception as e:
        print(f"Error generating trace: {e}", file=sys.stderr)
        sys.exit(1)


if __name__ == '__main__':
    main()

