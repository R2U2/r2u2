#!/usr/bin/env python3
"""
This file was generated by Cursor AI on 2026-04-29.

Sort benchmark results by total execution time and write every Nth filename.
"""

import argparse
import json
import math
from pathlib import Path


TIME_KEYS = [
    "egglog-encoding-time",
    "egglog-solver-time",
    "gurobi-encoding-time",
    "gurobi-solving-time",
]

STATUS_KEYS = [
    "egglog-status",
    "gurobi-status",
]


def is_timeout_or_memout(stats):
    """Return True if any status indicates timeout/memout."""
    for key in STATUS_KEYS:
        status = str(stats.get(key, "")).lower()
        if "timeout" in status or "memout" in status:
            return True
    return False


def finite_time(value):
    """Return finite float time or None if invalid."""
    try:
        parsed = float(value)
    except (TypeError, ValueError):
        return None
    if not math.isfinite(parsed):
        return None
    return parsed


def compute_total_runtime(stats):
    """Sum time fields when all are present and valid."""
    total = 0.0
    for key in TIME_KEYS:
        parsed = finite_time(stats.get(key))
        if parsed is None:
            return None
        total += parsed
    return total


def select_stats_object(result, stats_key):
    """Pick the stats object to use from each result item."""
    if stats_key:
        selected = result.get(stats_key)
        if isinstance(selected, dict):
            return selected
        return None

    for key, value in result.items():
        if key != "filename" and isinstance(value, dict):
            return value
    return None


def to_relative_benchmarks_path(filename):
    """Convert an absolute benchmark path to path relative to benchmarks/."""
    marker = "/benchmarks/"
    if marker in filename:
        return filename.split(marker, 1)[1]
    return filename


def sort_and_sample_results(input_path, output_path, every_n, stats_key):
    with input_path.open("r", encoding="utf-8") as f:
        data = json.load(f)

    results = data.get("results", [])
    if not isinstance(results, list):
        raise ValueError("Input JSON must contain a list at key 'results'.")

    computed = []
    max_non_timeout_runtime = 0.0

    for result in results:
        filename = result.get("filename")
        stats = select_stats_object(result, stats_key)
        if not filename or stats is None:
            continue

        timeout_or_memout = is_timeout_or_memout(stats)
        runtime = compute_total_runtime(stats)
        if (not timeout_or_memout) and runtime is not None:
            max_non_timeout_runtime = max(max_non_timeout_runtime, runtime)

        computed.append(
            {
                "filename": filename,
                "runtime": runtime,
                "timeout_or_memout": timeout_or_memout,
                "original": result,
            }
        )

    sentinel_runtime = max_non_timeout_runtime + 1.0
    for item in computed:
        if item["timeout_or_memout"] or item["runtime"] is None:
            item["effective_runtime"] = sentinel_runtime
        else:
            item["effective_runtime"] = item["runtime"]

    computed.sort(key=lambda item: (item["effective_runtime"], item["filename"]))
    sampled = computed[every_n - 1 :: every_n]

    output_filenames = []
    for item in sampled:
        output_filenames.append(to_relative_benchmarks_path(item["filename"]))

    with output_path.open("w", encoding="utf-8") as f:
        for filename in output_filenames:
            f.write(f"{filename}\n")

    return len(computed), len(output_filenames), sentinel_runtime


def main():
    parser = argparse.ArgumentParser(
        description="Sort results by total runtime and write every Nth file."
    )
    parser.add_argument("input_json", type=Path, help="Path to input JSON file.")
    parser.add_argument(
        "-o",
        "--output",
        type=Path,
        default=None,
        help="Output text path (default: <input_stem>_every10th_filenames.txt).",
    )
    parser.add_argument(
        "--every-n",
        type=int,
        default=10,
        help="Select every Nth element from sorted list (default: 10).",
    )
    parser.add_argument(
        "--stats-key",
        default="optimal-incomplete-ac",
        help="Stats object key inside each result (default: optimal-incomplete-ac).",
    )

    args = parser.parse_args()
    if args.every_n <= 0:
        raise ValueError("--every-n must be > 0")

    output_path = args.output
    if output_path is None:
        output_path = args.input_json.with_name(
            f"{args.input_json.stem}_every10th_filenames.txt"
        )

    total, selected, sentinel = sort_and_sample_results(
        args.input_json, output_path, args.every_n, args.stats_key
    )

    print(f"Processed {total} results.")
    print(f"Wrote {selected} sampled filenames to {output_path}.")
    print(f"Timeout/memout effective runtime: {sentinel:.6f}s")


if __name__ == "__main__":
    main()
