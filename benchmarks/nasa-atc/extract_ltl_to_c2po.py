def extract_ltlspec_formulas(smv_file: Path) -> List[Tuple[str, str]]:
    """
    Extract LTLSPEC formulas from an SMV file.
    Returns a list of (name, formula) tuples.
    """
    formulas = []
    with open(smv_file, 'r') as f:
        content = f.read()
    
    # Split by LTLSPEC to find all formulas
    # Pattern: LTLSPEC NAME name := ... ;
    # Name can contain letters, digits, underscores, and hyphens
    parts = re.split(r'LTLSPEC\s+NAME\s+([\w-]+)\s*:=\s*', content)
    
    # First part is everything before the first LTLSPEC, skip it
    for i in range(1, len(parts), 2):
        if i + 1 < len(parts):
            name = parts[i]
            # Find the formula part (everything until the next semicolon that ends the formula)
            formula_text = parts[i + 1]
            
            # Find the semicolon that ends this formula
            semicolon_pos = formula_text.find(';')
            if semicolon_pos != -1:
                formula = formula_text[:semicolon_pos]
            else:
                # No semicolon found, take everything (shouldn't happen in valid SMV)
                formula = formula_text
            
            # Remove comments
            formula = re.sub(r'--.*?$', '', formula, flags=re.MULTILINE)
            # Clean up whitespace
            formula = re.sub(r'\s+', ' ', formula).strip()
            if formula:
                formulas.append((name, formula))
    
    return formulas


