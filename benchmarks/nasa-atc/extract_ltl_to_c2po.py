def process_smv_files(smv_dir: Path, output_dir: Path):
    """
    Process all SMV files in the directory and generate C2PO files.
    Filters formulas to keep only 'far' versions when formulas have {far,mid,near} in their names.
    """
    smv_files = list(smv_dir.glob("**/*.smv"))
    
    if not smv_files:
        print(f"No SMV files found in {smv_dir}")
        return
    
    print(f"Found {len(smv_files)} SMV file(s)")
    
    # First, collect all formulas from all SMV files
    all_formulas = []
    for smv_file in smv_files:
        formulas = extract_ltlspec_formulas(smv_file)
        all_formulas.extend(formulas)
    
    print(f"Found {len(all_formulas)} total LTLSPEC formula(s)")
    
    # Filter to keep only 'far' versions when formulas have {far,mid,near} in their names
    filtered_formulas = filter_far_only(all_formulas)
    print(f"Filtered to {len(filtered_formulas)} formula(s) (keeping only 'far' versions)")
    
    # Filter to keep only 'ac1' versions when formulas have {ac1,ac2,ac3,ac4} in their names
    filtered_formulas = filter_ac1_only(filtered_formulas)
    print(f"Filtered to {len(filtered_formulas)} formula(s) (keeping only 'ac1' versions)")
    
    # Filter to keep only 'atc' versions when formulas have {atc,self} in their names
    filtered_formulas = filter_atc_only(filtered_formulas)
    print(f"Filtered to {len(filtered_formulas)} formula(s) (keeping only 'atc' versions)")
    
    # First, collect all boolean variables from all SMV files
    all_boolean_vars = set()
    for smv_file in smv_files:
        boolean_vars = extract_boolean_variables_from_smv(smv_file)
        all_boolean_vars.update(boolean_vars)
    
    print(f"Found {len(all_boolean_vars)} boolean variable(s) across all SMV files")
    
    # Create output directories
    for dir_name in DIR_NAMES:
        (output_dir / dir_name).mkdir(parents=True, exist_ok=True)
    
    total_formulas = 0
    
    # Process filtered formulas
    for name, formula in filtered_formulas:
        sanitized_name = sanitize_filename(name)
        
        # Get boolean vars (we'll use all_boolean_vars since we're processing formulas from multiple files)
        combined_boolean_vars = all_boolean_vars
        
        # Check if the formula has temporal operators after conversion
        # Convert with first interval to check
        test_formula, _ = convert_ltl_to_c2po(formula, INTERVALS[0][0], INTERVALS[0][1], combined_boolean_vars)
        
        # Check for temporal operators: G[...], F[...], U[...], R[...], H[...], O[...], S[...], T[...]
        temporal_pattern = r'[GUFHROST]\[[^\]]+\]'
        has_temporal = bool(re.search(temporal_pattern, test_formula))
        
        if not has_temporal:
            print(f"    Skipping {name} (no temporal operators)")
            continue
        
        total_formulas += 1
        
        # Generate 5 versions with different intervals
        for (lb, ub), dir_name in zip(INTERVALS, DIR_NAMES):
            c2po_formula, variables = convert_ltl_to_c2po(formula, lb, ub, combined_boolean_vars)
            
            # Generate C2PO file content with boolean variable information
            c2po_content = generate_c2po_file(c2po_formula, variables, combined_boolean_vars, name)
            
            # Write to file
            output_file = output_dir / dir_name / f"{sanitized_name}.c2po"
            with open(output_file, 'w') as f:
                f.write(c2po_content + '\n')
        
        print(f"    Generated C2PO for: {name}")
    
    print(f"\nTotal formulas processed: {total_formulas}")
    print(f"Total C2PO files generated: {total_formulas * len(INTERVALS)}")


def main():
    # Get the script directory
    script_dir = Path(__file__).parent
    smv_dir = script_dir / "smv"
    output_dir = script_dir / "c2po"
    
    if not smv_dir.exists():
        print(f"Error: SMV directory not found: {smv_dir}")
        return
    
    # Create output directory
    output_dir.mkdir(parents=True, exist_ok=True)
    
    process_smv_files(smv_dir, output_dir)
    print(f"\nDone! C2PO files written to {output_dir}")


if __name__ == "__main__":
    main()

