def extract_boolean_variables_from_smv(smv_file: Path) -> Set[str]:
    """
    Extract all boolean variable declarations from an SMV file.
    Returns a set of variable names (with dots replaced by underscores).
    Includes:
    - Variables explicitly declared as : boolean;
    - Variables defined with := that are boolean expressions (contain =, |, &, !, etc.)
    """
    boolean_vars = set()
    
    with open(smv_file, 'r') as f:
        content = f.read()
    
    # Pattern 1: variable_name : boolean;
    pattern1 = r'\b([a-zA-Z_][a-zA-Z0-9_.]*)\s*:\s*boolean\s*;'
    for match in re.finditer(pattern1, content, re.IGNORECASE):
        var_name = match.group(1)
        # Replace dots with underscores
        var_name = var_name.replace('.', '_')
        boolean_vars.add(var_name)
    
    # Pattern 2: variable_name := boolean_expression;
    # Look for DEFINE statements that are boolean expressions
    # Boolean expressions typically contain: =, !=, |, &, !, TRUE, FALSE
    pattern2 = r'\b([a-zA-Z_][a-zA-Z0-9_.]*)\s*:=\s*([^;]+);'
    for match in re.finditer(pattern2, content, re.IGNORECASE):
        var_name = match.group(1)
        definition = match.group(2)
        # Replace dots with underscores
        var_name = var_name.replace('.', '_')
        # Check if the definition looks like a boolean expression
        # It should contain boolean operators or comparisons
        if re.search(r'\b(TRUE|FALSE|true|false)\b', definition) or \
           re.search(r'[=!]|[\|\&]|!', definition):
            # Additional check: if it contains arithmetic operators without comparisons, it might be int
            # But if it has =, !=, |, &, !, TRUE, FALSE, it's likely boolean
            if not re.search(r'[+\-*/]\s*\d+|\d+\s*[+\-*/]', definition):
                # No arithmetic operations with numbers, likely boolean
                boolean_vars.add(var_name)
    
    return boolean_vars


def extract_variables(formula: str) -> Set[str]:
    """
    Extract all variable identifiers from the formula.
    Excludes operators, constants, and numbers.
    Replaces dots with underscores in variable names (C2PO doesn't support dots).
    """
    variables = set()
    
    # Excluded keywords (including true/false which are C2PO boolean literals)
    excluded = {'G', 'F', 'X', 'U', 'R', 'H', 'O', 'S', 'T', 'M', 'true', 'false', 'next', 'TRUE', 'FALSE'}
    
    # Pattern to match identifiers (including dots)
    pattern = r'\b([a-zA-Z_][a-zA-Z0-9_.]*)\b'
    
    for match in re.finditer(pattern, formula):
        identifier = match.group(1)
        
        # Skip if it's an excluded keyword
        if identifier in excluded:
            continue
        
        # Skip if it's a number
        if identifier.isdigit():
            continue
        
        # Skip if it's already a proposition (a0, a1, etc.) - these shouldn't appear in LTLSPEC
        if re.match(r'^a\d+$', identifier):
            continue
        
        # Replace dots with underscores (C2PO doesn't support dots in identifiers)
        identifier = identifier.replace('.', '_')
        variables.add(identifier)
    
    return variables


def convert_x_to_f11(formula: str) -> str:
    """
    Convert all X operators to F[1,1].
    """
    # Replace standalone X (not part of other words)
    # X can appear as: X, X X, X X X, etc.
    def replace_x(match):
        x_count = len(match.group(0).split())
        if x_count == 1:
            return "F[1,1]"
        else:
            # Multiple X operators: X X becomes F[1,1] F[1,1]
            return " ".join(["F[1,1]"] * x_count)
    
    # Pattern to match one or more X operators separated by spaces
    result = re.sub(r'\bX(\s+X)*\b', replace_x, formula)
    return result


def convert_operators_to_c2po(formula: str) -> str:
    """
    Convert LTL/SMV operators to C2PO operators.
    - & -> &&
    - | -> ||
    - = -> ==
    - != stays !=
    - TRUE -> true
    - FALSE -> false
    Also replaces dots in variable names with underscores.
    """
    # First, convert TRUE/FALSE to true/false (case-insensitive, whole word only)
    formula = re.sub(r'\bTRUE\b', 'true', formula)
    formula = re.sub(r'\bFALSE\b', 'false', formula)
    
    # Replace dots in variable names with underscores (C2PO doesn't support dots)
    # Pattern: identifier with dots (but not in operators or numbers)
    def replace_dots(match):
        identifier = match.group(0)
        # Only replace if it's not part of an operator or number
        if '.' in identifier and not identifier.replace('.', '').isdigit():
            return identifier.replace('.', '_')
        return identifier
    
    # Replace dots in identifiers (word characters and dots)
    formula = re.sub(r'\b[a-zA-Z_][a-zA-Z0-9_.]*\b', replace_dots, formula)
    
    # Replace & with && (but not && which is already correct)
    # Need to match & that's not followed by &
    formula = re.sub(r'&(?!&)', '&&', formula)
    
    # Replace | with || (but not || which is already correct, and not | in !=)
    # Need to be careful - | can appear in !=
    formula = re.sub(r'(?<!!)\b\|\b(?!\|)', '||', formula)
    
    # Replace = with == (but not == which is already correct, and not = in !=, <=, >=, :=, ->)
    # Pattern: = that's not part of ==, !=, <=, >=, :=, ->
    # We need to be careful: -> contains =, so we need to avoid matching = in ->
    # Strategy: replace = with ==, but skip if it's part of ->, :=, <=, >=, !=, ==
    # Use a simpler approach: replace = with ==, then fix -> back
    original_formula = formula
    formula = re.sub(r'=', '==', formula)
    # Fix -> which got converted to -==
    formula = re.sub(r'-==', '->', formula)
    # Fix := which got converted to :==
    formula = re.sub(r':==', ':=', formula)
    # Fix <= which got converted to <==
    formula = re.sub(r'<==', '<=', formula)
    # Fix >= which got converted to >==
    formula = re.sub(r'>==', '>=', formula)
    # Fix != which got converted to !==
    formula = re.sub(r'!==', '!=', formula)
    # Fix == which got converted to ====
    formula = re.sub(r'====', '==', formula)
    
    return formula


def add_intervals_to_temporal_operators(formula: str, lb: int, ub: int) -> str:
    """
    Add intervals to temporal operators (G, F, U, R).
    X operators should already be converted to F[1,1] before calling this.
    """
    # Replace G (Global) - unary operator
    formula = re.sub(r'\bG\s+(?![\[\(])', f'G[{lb}, {ub}] ', formula)
    formula = re.sub(r'\bG\s*\(', f'G[{lb}, {ub}] (', formula)
    
    # Replace F (Future) - unary operator, but skip if already has interval
    formula = re.sub(r'\bF\s+(?![\[\(])', f'F[{lb}, {ub}] ', formula)
    formula = re.sub(r'\bF\s*\(', f'F[{lb}, {ub}] (', formula)
    
    # Replace U (Until) - binary operator
    formula = re.sub(r'\bU\s+(?![\[\(])', f'U[{lb}, {ub}] ', formula)
    formula = re.sub(r'\bU\s*\(', f'U[{lb}, {ub}] (', formula)
    
    # Replace R (Release) - binary operator
    formula = re.sub(r'\bR\s+(?![\[\(])', f'R[{lb}, {ub}] ', formula)
    formula = re.sub(r'\bR\s*\(', f'R[{lb}, {ub}] (', formula)
    
    return formula


def convert_int_to_bool_in_boolean_context(formula: str, boolean_vars: Set[str]) -> str:
    """
    Convert integer variables used in boolean contexts to boolean comparisons.
    For variables not in boolean_vars that are used with &&, ||, !, or temporal operators,
    wrap them in (var != 0) to convert to boolean.
    But skip variables that are already in comparisons (==, !=, >, <, >=, <=).
    """
    # First, extract all variables from the formula
    all_vars = extract_variables(formula)
    int_vars = sorted(all_vars - boolean_vars, key=len, reverse=True)  # Longest first to avoid partial matches
    
    # For each int variable, wrap it in (var != 0) when used in boolean contexts
    # But NOT when it's in a comparison (==, !=, >, <, >=, <=)
    # Boolean variables should NOT be wrapped
    for var in int_vars:
        # Use word boundaries to match the variable name exactly
        var_pattern = r'\b' + re.escape(var) + r'\b'
        
        # Replace patterns where var appears in boolean context
        # But avoid wrapping if var is part of a comparison
        
        # Pattern 1: && var or || var (but not if var is followed by comparison operator)
        # Negative lookahead to ensure var is not followed by ==, !=, >, <, >=, <=
        # Also need to check if var is the right side of a comparison (== var, != var, etc.)
        def replace_var_after_op(match):
            op = match.group(1)
            # Group 1: operator, Group 2: variable, Group 3: None (from negative lookahead)
            var_part = match.group(2) if match.group(2) is not None else var
            # Check if this var is the right side of a comparison by looking at context before the match
            start_pos = match.start()
            # Look back to see if there's a comparison operator before this
            context_start = max(0, start_pos - 50)
            before_match = formula[context_start:start_pos]
            # If we see a comparison operator (==, !=, etc.) followed by whitespace and then our var name, don't wrap
            # Pattern: comparison_op whitespace ... var (where var is at the end, meaning it's the right side)
            # We need to check if the var name appears right after a comparison operator
            # Look for: comparison_op, then whitespace, then possibly other tokens, ending with our var
            # Actually, simpler: if we see "== " or "!= " etc. and then our var is next, don't wrap
            if re.search(r'(==|!=|>=|<=|>|<)\s+[^&|!()]*' + re.escape(var) + r'\s*$', before_match):
                # This var is the right side of a comparison, don't wrap
                return f'{op} {var_part}'
            return f'{op} (({var_part}) != 0)'
        
        # Pattern: (operator) whitespace (variable) (negative lookahead) (lookahead)
        # Group 1: operator, Group 2: None (from negative lookahead), Group 3: variable
        formula = re.sub(r'(&&|\|\|)\s*(' + var_pattern + r')(?!\s*(==|!=|>|<|>=|<=))(?=\s|$|&&|\|\||!|\)|->|;)', 
                        replace_var_after_op, formula)
        
        # Pattern 2: ! var (but not != and not if var is in a comparison)
        formula = re.sub(r'!\s+' + var_pattern + r'(?!\s*(==|!=|>|<|>=|<=))(?=\s|$|&&|\|\||!|\)|->|;)', 
                        r'!((' + var + r') != 0)', formula)
        
        # Pattern 3: var && or var || (but not if var is part of a comparison)
        # We need to check if var is the right side of a comparison (== var &&)
        def replace_var_before_op(match):
            var_part = var  # Use the variable from the outer scope
            # The pattern has groups: (var_pattern)(negative_lookahead_with_group)(operator)
            # Group 1 is the variable, group 2 is None (from negative lookahead), group 3 is the operator
            op = match.group(3) if len(match.groups()) >= 3 else ('&&' if '&&' in match.group(0) else '||')
            # Check if this var is the right side of a comparison by looking at context before the match
            start_pos = match.start()
            # Look at a larger context that includes the match position to see if var is after a comparison op
            context_start = max(0, start_pos - 50)
            # Include a bit after the match to see the full context
            context_end = min(len(formula), start_pos + len(match.group(0)) + 10)
            context = formula[context_start:context_end]
            # Check if we see pattern: comparison_op whitespace var (where var is at the match position)
            # The var should appear right after a comparison operator
            var_pos_in_context = start_pos - context_start
            before_var = context[:var_pos_in_context]
            # If the text right before our var ends with a comparison operator followed by whitespace, it's a comparison
            if re.search(r'(==|!=|>=|<=|>|<)\s+$', before_var):
                # This var is the right side of a comparison, don't wrap
                return f'{var_part} {op}'
            return f'(({var_part}) != 0) {op}'
        
        # Pattern: (var) (negative lookahead for comparison ops) whitespace (operator)
        # The negative lookahead (?!...) doesn't create a capture group
        formula = re.sub(r'(' + var_pattern + r')(?!\s*(==|!=|>|<|>=|<=))\s*(&&|\|\|)', 
                        replace_var_before_op, formula)
        
        # Pattern 4: Temporal operators followed by var: G[...] var, F[...] var, etc.
        # But not if var is immediately followed by a comparison operator
        # Also need to check if var is on the right side of a comparison (== var, != var, etc.)
        def replace_var_after_temporal(match):
            temporal_op = match.group(1)
            var_part = var
            # Check if this var is on the right side of a comparison by looking at context before the match
            start_pos = match.start()
            context_start = max(0, start_pos - 50)
            before_match = formula[context_start:start_pos]
            # If we see a comparison operator (==, !=, etc.) followed by whitespace ending with our var, don't wrap
            if re.search(r'(==|!=|>=|<=|>|<)\s+[^&|!()]*' + re.escape(var) + r'\s*$', before_match):
                # This var is the right side of a comparison, don't wrap
                return f'{temporal_op} {var_part}'
            return f'{temporal_op} (({var_part}) != 0)'
        
        formula = re.sub(r'([GUFHR]\[[^\]]+\])\s+' + var_pattern + r'(?!\s*(==|!=|>|<|>=|<=))(?=\s|$|&&|\|\||!|\)|->|;)', 
                        replace_var_after_temporal, formula)
        
        # Pattern 5: var -> (implication requires boolean operands)
        # Wrap var when it's the left side of ->
        # But not if var is on the right side of a comparison (== var ->)
        def replace_var_before_implication(match):
            var_part = match.group(1)
            # Check if this var is on the right side of a comparison by looking at context before the match
            start_pos = match.start()
            context_start = max(0, start_pos - 50)
            before_match = formula[context_start:start_pos]
            # If we see a comparison operator (==, !=, etc.) followed by whitespace at the end, the var is on the right side
            # Pattern: comparison_op whitespace (possibly with other content) ending with whitespace
            if re.search(r'(==|!=|>=|<=|>|<)\s+\S*\s*$', before_match):
                # This var is the right side of a comparison, don't wrap
                return f'{var_part} ->'
            return f'(({var_part}) != 0) ->'
        
        formula = re.sub(r'(' + var_pattern + r')(?!\s*(==|!=|>|<|>=|<=))\s*->', 
                        replace_var_before_implication, formula)
        
        # Pattern 6: -> var (right side of implication also needs to be boolean, but the right side is usually a formula)
        # This is handled by other patterns or the right side is already a boolean expression
    
    return formula


def convert_ltl_to_c2po(formula: str, lb: int, ub: int, boolean_vars: Set[str]) -> Tuple[str, Set[str]]:
    """
    Convert an LTL formula to C2PO format.
    Returns (c2po_formula, variables_set).
    """
    # Step 1: Extract variables (before any conversions)
    variables = extract_variables(formula)
    
    # Step 2: Convert X to F[1,1]
    formula = convert_x_to_f11(formula)
    
    # Step 3: Convert operators to C2PO syntax
    formula = convert_operators_to_c2po(formula)
    
    # Step 4: Wrap comparisons after temporal operators
    # Pattern: F[...] F[...] ... var == value should become F[...] F[...] ... (var == value)
    # This ensures temporal operators apply to the entire comparison
    def wrap_comparison_after_temporal(match):
        temporal_ops = match.group(1)  # e.g., "F[1,1] F[1,1]"
        var = match.group(2)
        op = match.group(3)
        value = match.group(4).strip()
        return f'{temporal_ops} ({var} {op} {value})'
    
    # Match: temporal operator(s) followed by variable followed by comparison operator and value
    formula = re.sub(r'((?:[GUFHR]\[[^\]]+\]\s*)+)(\w+)\s*(==|!=|>|<|>=|<=)\s*([^&|!)]+)', 
                    wrap_comparison_after_temporal, formula)
    
    # Step 5: Convert int variables in boolean contexts to (var != 0)
    formula = convert_int_to_bool_in_boolean_context(formula, boolean_vars)
    
    # Step 6: Add intervals to temporal operators
    formula = add_intervals_to_temporal_operators(formula, lb, ub)
    
    # Step 6: Clean up whitespace
    formula = re.sub(r'\s+', ' ', formula).strip()
    
    return formula, variables


def sanitize_filename(name: str) -> str:
    """
    Sanitize a formula name to be a valid filename.
    """
    # Replace invalid filename characters
    name = re.sub(r'[<>:"/\\|?*]', '_', name)
    # Replace spaces with underscores
    name = name.replace(' ', '_')
    return name.lower()


def fix_variable_types_from_formula(formula: str, variables: Set[str], boolean_vars: Set[str]) -> Set[str]:
    """
    Analyze the formula to determine correct variable types.
    If a variable is compared to an integer (variable or constant), it should be int.
    If a variable is compared to a boolean, it should be bool.
    Returns updated set of boolean variables.
    """
    import re
    updated_boolean_vars = boolean_vars.copy()
    
    # Find all comparisons in the formula
    # Pattern 1: var1 == var2, var1 != var2, etc. (variable to variable)
    comparison_pattern = r'(\w+)\s*(==|!=)\s*(\w+)'
    
    for match in re.finditer(comparison_pattern, formula):
        var1 = match.group(1)
        var2 = match.group(3)
        
        # Skip if not actual variables (could be numbers, operators, etc.)
        if var1 not in variables or var2 not in variables:
            continue
        
        var1_is_bool = var1 in updated_boolean_vars
        var2_is_bool = var2 in updated_boolean_vars
        
        # If one is bool and the other is int, fix the type
        if var1_is_bool and not var2_is_bool:
            # var1 is bool but var2 is int, so var1 should be int
            updated_boolean_vars.discard(var1)
        elif not var1_is_bool and var2_is_bool:
            # var1 is int but var2 is bool, so var2 should be int
            updated_boolean_vars.discard(var2)
    
    # Pattern 2: var == integer_constant or var != integer_constant
    # If a variable is compared to an integer constant, it should be int
    int_constant_pattern = r'(\w+)\s*(==|!=|>|<|>=|<=)\s*(\d+)'
    for match in re.finditer(int_constant_pattern, formula):
        var = match.group(1)
        if var in variables and var in updated_boolean_vars:
            # Variable is bool but compared to integer constant, should be int
            updated_boolean_vars.discard(var)
    
    return updated_boolean_vars


def generate_c2po_file(formula: str, variables: Set[str], boolean_vars: Set[str], name: str) -> str:
    """
    Generate a complete C2PO file from a formula and its variables.
    Separates variables into bool and int types.
    Fixes variable types based on how they're used in the formula.
    """
    lines = []
    
    # Fix variable types based on formula usage
    boolean_vars = fix_variable_types_from_formula(formula, variables, boolean_vars)
    
    # INPUT section
    lines.append("INPUT")
    if variables:
        # Separate variables into bool and int
        bool_vars = sorted([v for v in variables if v in boolean_vars])
        int_vars = sorted([v for v in variables if v not in boolean_vars])
        
        # Add boolean variables
        if bool_vars:
            bool_list = ", ".join(bool_vars)
            lines.append(f"    {bool_list}: bool;")
        
        # Add integer variables
        if int_vars:
            int_list = ", ".join(int_vars)
            lines.append(f"    {int_list}: int;")
        
        # If no variables after filtering, add a dummy
        if not bool_vars and not int_vars:
            lines.append("    dummy: int;")
    else:
        # If no variables, add a dummy variable to make it valid
        lines.append("    dummy: int;")
    
    lines.append("")
    
    # FTSPEC section
    # Replace hyphens in name with underscores (C2PO identifiers can't have hyphens)
    spec_name = name.replace('-', '_')
    lines.append(f"FTSPEC")
    lines.append(f"    {spec_name}: {formula};")
    
    return "\n".join(lines)


def filter_far_only(formulas: List[Tuple[str, str]]) -> List[Tuple[str, str]]:
    """
    Filter formulas to keep only 'far' versions when formulas have {far,mid,near} in their names.
    For any formula name that contains 'far', 'mid', or 'near', only keep the one with 'far'.
    """
    directions = ['far', 'mid', 'near']
    filtered = []
    seen_base_names = set()
    
    for name, formula in formulas:
        name_lower = name.lower()
        
        # Check if this formula contains any direction
        contains_direction = any(direction in name_lower for direction in directions)
        
        if contains_direction:
            # Extract base name by removing direction words
            base_name = name_lower
            for direction in directions:
                base_name = base_name.replace(direction, '')
            
            # If we've seen this base name before, check if we should replace it
            if base_name in seen_base_names:
                # Find the existing formula with this base name
                existing_idx = None
                for i, (existing_name, _) in enumerate(filtered):
                    existing_base = existing_name.lower()
                    for direction in directions:
                        existing_base = existing_base.replace(direction, '')
                    if existing_base == base_name:
                        existing_idx = i
                        break
                
                if existing_idx is not None:
                    existing_name = filtered[existing_idx][0].lower()
                    # If existing is not 'far' and current is 'far', replace it
                    if 'far' not in existing_name and 'far' in name_lower:
                        filtered[existing_idx] = (name, formula)
                    # Otherwise, skip the current one (keep existing)
                    continue
            
            # First time seeing this base name, add it
            seen_base_names.add(base_name)
            filtered.append((name, formula))
        else:
            # Formula doesn't contain direction words, keep it
            filtered.append((name, formula))
    
    return filtered


def filter_ac1_only(formulas: List[Tuple[str, str]]) -> List[Tuple[str, str]]:
    """
    Filter formulas to keep only 'ac1' versions when formulas have {ac1,ac2,ac3,ac4} in their names.
    For any formula name that contains 'ac1', 'ac2', 'ac3', or 'ac4', only keep the one with 'ac1'.
    """
    acs = ['ac1', 'ac2', 'ac3', 'ac4']
    filtered = []
    seen_base_names = set()
    
    for name, formula in formulas:
        name_lower = name.lower()
        
        # Check if this formula contains any AC number
        contains_ac = any(ac in name_lower for ac in acs)
        
        if contains_ac:
            # Extract base name by removing AC numbers
            base_name = name_lower
            for ac in acs:
                base_name = base_name.replace(ac, '')
            
            # If we've seen this base name before, check if we should replace it
            if base_name in seen_base_names:
                # Find the existing formula with this base name
                existing_idx = None
                for i, (existing_name, _) in enumerate(filtered):
                    existing_base = existing_name.lower()
                    for ac in acs:
                        existing_base = existing_base.replace(ac, '')
                    if existing_base == base_name:
                        existing_idx = i
                        break
                
                if existing_idx is not None:
                    existing_name = filtered[existing_idx][0].lower()
                    # If existing is not 'ac1' and current is 'ac1', replace it
                    if 'ac1' not in existing_name and 'ac1' in name_lower:
                        filtered[existing_idx] = (name, formula)
                    # Otherwise, skip the current one (keep existing)
                    continue
            
            # First time seeing this base name, add it
            seen_base_names.add(base_name)
            filtered.append((name, formula))
        else:
            # Formula doesn't contain AC numbers, keep it
            filtered.append((name, formula))
    
    return filtered


